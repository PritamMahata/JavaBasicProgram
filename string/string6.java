package string;
// convert a sting to toLowercase and toUppercase
public class string6 {
    public static void main(String[] args) {
        String s = "Pritam";
        System.out.println(s.toLowerCase()); // pritam
        System.out.println(s.toUpperCase()); // PRITAM
        System.out.println(s); // Pritam
    }
}